package aula2;

public class CalculoDobro {
    
    //Atributo local
    int d;
    
    /*
     * Construtor de Classe, que recebe um valor i e atribui ao valor local(d)
    */
    
    public CalculoDobro(int x) {
        d = x;
    }

    //Metodo que calcula o dobro do numero e devolve um valor

    public int Dobro() {
        return d * 2;
    }
}
        